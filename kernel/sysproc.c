uint64
sys_exit(void)
{
  int n;
  argint(0, &n);
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  argaddr(0, &p);
  return wait(p);
}

uint64
sys_sbrk(void)
{
  uint64 addr;
  int n;

  argint(0, &n);
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  argint(0, &n);
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(killed(myproc())){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  argint(0, &pid);
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

//  The sys_trace system call is used to enable tracing of system calls
//  based on a provided mask. The mask specifies which system calls should
//  be traced. Each bit in the mask corresponds to a specific system call
//  number. For example, to trace the fork system call, the mask should
//  include the bit corresponding to SYS_fork.

//  Arguments:
//     int mask: The tracing mask indicating which system calls to trace.
//               Each bit corresponds to a specific system call number.
uint64
sys_trace(void)
{
  int mask;
  
  //获取追踪的mask
  argint(0, &mask);
  //将mask保存在本进程的proc中
  myproc()->trace_mask = mask;
  return 0;
}
